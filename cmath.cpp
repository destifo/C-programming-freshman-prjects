#include <iostream>
#include <cmath>
using namespace std;

int main()
{

    cout<<pow(2, 3)<<endl;
    cout<<sqrt(16)<<endl;
    cout<<round(7.4)<<endl;//round to the nearest whole no
    cout<<ceil(7.4)<<endl;//round to the highest no
    cout<<floor(9.8)<<endl;//round to the lowest whole no
    cout<<fmax(7, 6)<<endl;//displays the largest of the two numbers
    cout<<fmin(7, 6)<<endl;
    return 0;
}


