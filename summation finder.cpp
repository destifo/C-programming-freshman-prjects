#include <iostream>
#include <math.h>
using namespace std;

int main()
{
    int sum = 0, count1 = 0, n =1, c;
    cout<< "Enter the count"<<endl;
    cin>>c;
    int reminder = n%2;
    if(reminder==1)
    {
        n = n++;
        sum=sum+n;
        count1=count1++;
        while(count1==c)
            cout<< "The sum is "<<sum<<endl;
    }
    else
    {
        count1= count1++, sum = sum + n;
        while(count1==c)
        {
            cout<< "The sum is "<<sum<<endl;
        }
    }
    return 0;
}
